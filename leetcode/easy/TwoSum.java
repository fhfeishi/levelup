package leetcode.easy;

public class TwoSum {
    public TwoSum() {}
    public int add(int a, int b) {
        return a + b;
    }
    public static void main(String[] args){
        TwoSum twoSum = new TwoSum();
        int result = twoSum.add(5, 7);
        System.out.println("5+7=" + result);
    }
}
